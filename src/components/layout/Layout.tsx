/**
 * Main Layout Component
 * Provides the application shell with sidebar navigation
 */

import React from 'react';
import { Link, useLocation } from 'react-router-dom';
import { useApp } from '@/contexts';

interface LayoutProps {
  children: React.ReactNode;
}

/** Navigation items */
const navItems = [
  { path: '/', label: 'Dashboard', icon: DashboardIcon },
  { path: '/wallets', label: 'Wallets', icon: WalletIcon },
  { path: '/create-wallet', label: 'Create Wallet', icon: PlusIcon },
  { path: '/sign', label: 'Sign Transaction', icon: SignIcon },
  { path: '/settings', label: 'Settings', icon: SettingsIcon },
];

export function Layout({ children }: LayoutProps): React.ReactElement {
  const location = useLocation();
  const { state, lock } = useApp();

  return (
    <div className="flex h-screen bg-slate-900">
      {/* Sidebar */}
      <aside className="w-64 bg-slate-800 border-r border-slate-700 flex flex-col">
        {/* Logo/Title */}
        <div className="p-4 border-b border-slate-700">
          <h1 className="text-xl font-bold text-white flex items-center gap-2">
            <span className="text-2xl">üîê</span>
            Wallet Room
          </h1>
          <div className="mt-1 flex items-center gap-2">
            <span
              className={`inline-block w-2 h-2 rounded-full ${
                state.mode === 'offline' ? 'bg-cyan-400' : 'bg-orange-400'
              }`}
            />
            <span className="text-xs text-slate-400 uppercase">
              {state.mode} Mode
            </span>
          </div>
        </div>

        {/* Navigation */}
        <nav className="flex-1 p-4 space-y-1">
          {navItems.map((item) => {
            const isActive = location.pathname === item.path;
            const Icon = item.icon;

            return (
              <Link
                key={item.path}
                to={item.path}
                className={`flex items-center gap-3 px-3 py-2 rounded-lg transition-colors ${
                  isActive
                    ? 'bg-blue-600 text-white'
                    : 'text-slate-300 hover:bg-slate-700 hover:text-white'
                }`}
              >
                <Icon className="w-5 h-5" />
                <span>{item.label}</span>
              </Link>
            );
          })}
        </nav>

        {/* Footer */}
        <div className="p-4 border-t border-slate-700">
          <button
            onClick={() => lock()}
            className="w-full flex items-center justify-center gap-2 px-3 py-2
                       text-slate-300 hover:text-white hover:bg-slate-700
                       rounded-lg transition-colors"
          >
            <LockIcon className="w-5 h-5" />
            <span>Lock Wallet</span>
          </button>
        </div>
      </aside>

      {/* Main Content */}
      <main className="flex-1 overflow-auto">
        <div className="p-6">{children}</div>
      </main>
    </div>
  );
}

